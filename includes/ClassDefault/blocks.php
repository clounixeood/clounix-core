function pnBlockShow($block, $args = array())
{

    //Carico il blocco
    pnBlockLoad($block);

    //Nome della funzione
    $func = "{$block}Display";

    //Se la funzione esiste
    if (function_exists($func)) { 
    
    //Caricamento corretto
    return $func($args);
    
    } else {
            
    return false;
    
    }

}
//////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////
function pnBlockLoad($block)
{	
    
    //Includo il file del blocco se necessario
    $incfile = 'blocks/'.$block.'.php';
        
    //Se il file non esiste ritorno falso
    if (!file_exists($incfile)) { return false; }
    
    //includo il file
    include_once $incfile;
    
    return true;
}
